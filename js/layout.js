/* --------------------------------------------------------
ドロワーメニューの開閉(モバイル)
----------------------------------------------------------*/
// ドロワー開
$('.fa-bars').on('click', function(){
    $('.drawer').animate({left: 0}, 200);
})
//ドロワー閉
$('.cancelDrawerLayer').on('click', function(){
    $('.drawer').animate({left: '-100vw'}, 200);
})

